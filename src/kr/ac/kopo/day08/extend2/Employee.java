package kr.ac.kopo.day08.extend2;

// 자동완성사용방법
// 생성자 자동완성 : Source -> Generate Constructor ~

public class Employee {
	String name;
	int salary;
	String grade;
	
	public Employee() {
	}
	
	public Employee(String name, int salary, String grade) {
		super();
		this.name = name;
		this.salary = salary;
		this.grade = grade;
	}
}
